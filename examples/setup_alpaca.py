import os
import sys
from pathlib import Path

def check_alpaca_packages():
    """Check if Alpaca packages are installed"""
    try:
        import alpaca
        return True
    except ImportError:
        return False

def create_env_file():
    """Create .env file with API credentials"""
    print("\nğŸ”§ Setting up your Alpaca API configuration")
    print("=" * 50)
    
    # Get credentials from user
    print("\nğŸ“‹ Enter your Alpaca API credentials:")
    print("   (Get these from: https://app.alpaca.markets/paper/dashboard/overview)")
    
    api_key = input("\nğŸ”‘ API Key: ").strip()
    secret_key = input("ğŸ” Secret Key: ").strip()
    
    if not api_key or not secret_key:
        print("âŒ API credentials cannot be empty!")
        return False
    
    # Create .env file
    env_content = f"""# Alpaca API Configuration
# Generated by setup script

ALPACA_API_KEY={api_key}
ALPACA_SECRET_KEY={secret_key}

# Trading Configuration
PAPER_TRADING=true
MAX_POSITIONS=5
POSITION_SIZE_PCT=0.1

# Risk Management
MAX_DAILY_LOSS_PCT=0.02
MAX_POSITION_VALUE=10000

# Data Settings
DEFAULT_LOOKBACK_DAYS=252
UPDATE_INTERVAL_MINUTES=15

# Logging
LOG_LEVEL=INFO
LOG_TO_FILE=true
"""
    
    try:
        env_file = Path(".env")
        with open(env_file, 'w') as f:
            f.write(env_content)
        
        print(f"âœ… Created .env file: {env_file.absolute()}")
        return True
        
    except Exception as e:
        print(f"âŒ Error creating .env file: {e}")
        return False

def test_connection():
    """Test Alpaca API connection"""
    try:
        from dotenv import load_dotenv
        load_dotenv()
        
        from alpaca_integration import AlpacaIntegratedTrader
        
        api_key = os.getenv('ALPACA_API_KEY')
        secret_key = os.getenv('ALPACA_SECRET_KEY')
        
        print("\nğŸ§ª Testing Alpaca API connection...")
        
        trader = AlpacaIntegratedTrader(api_key, secret_key, paper=True)
        account = trader.alpaca_trader.get_account_info()
        
        print("âœ… Connection successful!")
        print(f"   Account ID: {account['account_id']}")
        print(f"   Portfolio Value: ${account['portfolio_value']:,.2f}")
        print(f"   Buying Power: ${account['buying_power']:,.2f}")
        
        return True
        
    except Exception as e:
        print(f"âŒ Connection failed: {e}")
        return False

